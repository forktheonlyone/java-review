public class Cat extends Animal {

    public void sleep() {
        System.out.println("고양이가 구석에서 잠에 듭니다.");
    }

    public void makeSound() {
        System.out.println("고양이가 야~옹!하고 웁니다.");
        System.out.println("야~~옹!");
    }

    public void eat() {
        System.out.println("고양이가 밥을 먹습니다.");
    }

    public void play() {
        System.out.println("고양이가 뛰어 다닙니다.");
    }

    public void special() {
        System.out.println("고양이가 주먹으로 때립니다.");
        System.out.println("퍽! 퍽! 퍽!");
    }
}


